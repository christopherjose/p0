public class CustomerAccount {

    String customerId;
    String firstName;
    String lastName;
    String primaryPhoneNo;
    String emailAddress;
    String address;
    String address2;
    String city;
    String state;
    String zip;

    CustomerAccount(String customerId,
                    String firstName,
                    String lastName,
                    String primaryPhoneNo,
                    String emailAddress,
                    String address,
                    String address2,
                    String city,
                    String state,
                    String zip)
    {
        this.customerId=customerId;
        this.firstName=firstName;
        this.lastName=lastName;
        this.primaryPhoneNo=primaryPhoneNo;
        this.emailAddress=emailAddress;
        this.address=address;
        this.address2=address2;
        this.city=city;
        this.state=state;
        this.zip=zip;
    }




}
